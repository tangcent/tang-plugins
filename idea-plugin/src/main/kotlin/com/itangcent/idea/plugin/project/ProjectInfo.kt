package com.itangcent.idea.plugin.project


class ProjectInfo {
    var name: String? = null

    /**
     * 开发者
     */
    var author: String? = null

    /**
     * 项目类型
     */
    var type: String? = null

    /**
     * git url
     */
    var url: String? = null

    /**
     * 项目发布分支
     */
    var branch: String? = null

    /**
     * 项目描述
     */
    var description: String? = null

}
